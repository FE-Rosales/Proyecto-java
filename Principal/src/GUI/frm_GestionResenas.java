    private void btnGuardarResenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarResenaActionPerformed
        // TODO add your handling code here:
        
        try {
        if (cmbReserva.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar una reserva válida.");
            return;
        }

        String comentario = txtComentario.getText().trim();
        if (comentario.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, escriba un comentario.");
            return;
        }

        modelo.Reserva reservaSel = (modelo.Reserva) cmbReserva.getSelectedItem();
        
        // Evitamos que una misma reserva tenga múltiples reseñas
        if (reservaSel.getResena() != null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Esta reserva ya cuenta con una reseña registrada.");
            return;
        }

        int calificacion = (Integer) cmbCalificacion.getSelectedItem();

        // 1. Creamos la reseña
        modelo.Resena nuevaResena = new modelo.Resena(reservaSel, calificacion, comentario);

        // 2. La guardamos en el Gestor principal
        boolean exito = gestorResenas.registrar(nuevaResena);

        if (exito) {
            // 3. ¡La magia! Vinculamos la reseña directamente a la Reserva usando tu método
            reservaSel.registrarResena(nuevaResena);

            javax.swing.JOptionPane.showMessageDialog(this, "¡Reseña registrada con éxito!");
            txtComentario.setText("");
            actualizarTabla();
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Se alcanzó el límite de reseñas en el sistema.");
        }

    } catch (IllegalArgumentException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error: " + e.getMessage());
    }
        
        
    }//GEN-LAST:event_btnGuardarResenaActionPerformed

    private void btnEliminarResenaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarResenaActionPerformed
        // TODO add your handling code here:
        
        try {
        if (cmbReserva.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar una reserva de la lista.");
            return;
        }

        modelo.Reserva reservaSel = (modelo.Reserva) cmbReserva.getSelectedItem();

        // Verificamos si realmente tiene una reseña para eliminar
        if (reservaSel.getResena() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Esta reserva no tiene ninguna reseña registrada.");
            return;
        }

        // Pedimos confirmación
        int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this, 
            "¿Está seguro que desea eliminar la reseña?", 
            "Confirmar Eliminación", 
            javax.swing.JOptionPane.YES_NO_OPTION,
            javax.swing.JOptionPane.WARNING_MESSAGE);

        if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
            
            // 1. La eliminamos del gestor principal
            boolean exito = gestorResenas.eliminarPorReserva(reservaSel);

            if (exito) {
                // 2. ¡Importante! Rompemos el vínculo en la Reserva pasándole 'null'
                reservaSel.registrarResena(null);
                
                javax.swing.JOptionPane.showMessageDialog(this, "Reseña eliminada correctamente.");
                actualizarTabla();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error. No se pudo eliminar la reseña.");
            }
        }

    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error inesperado: " + e.getMessage());
    }
        
        
    }//GEN-LAST:event_btnEliminarResenaActionPerformed


