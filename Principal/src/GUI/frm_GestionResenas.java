    private void llenarCombos() {
        // 1. Llenamos las calificaciones del 1 al 5
        cmbCalificacion.removeAllItems();
        for (int i = 1; i <= 5; i++) {
            cmbCalificacion.addItem(i);
        }

        // 2. Llenamos el combo con las reservas que existen en el sistema
        cmbReserva.removeAllItems();
        modelo.Reserva[] reservasExistentes = gestorReservas.listarTodas();
        if (reservasExistentes != null) {
            for (modelo.Reserva r : reservasExistentes) {
                if (r != null) {
                    cmbReserva.addItem(r);
                }
            }
        }
    }
    
    private void actualizarTabla() {
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblResenas.getModel();
    modelo.setRowCount(0);
    
    modelo.Resena[] lista = gestorResenas.listarTodas();
    if (lista != null) {
        for (modelo.Resena r : lista) {
            if (r != null) {
                // Obtenemos el titular de la reserva (el primer cliente del arreglo)
                String nombreCliente = "Sin titular";
                modelo.Cliente[] clientesReserva = r.getReserva().getClientes();
                if (clientesReserva != null && clientesReserva.length > 0 && clientesReserva[0] != null) {
                    nombreCliente = clientesReserva[0].getNombres() + " " + clientesReserva[0].getApellidos();
                }

                modelo.addRow(new Object[]{
                    r.getFecha(),
                    nombreCliente,
                    r.getPaquete().getNombre(),
                    r.getCalificacion(),
                    r.getComentario(),
                    r.esPositiva() ? "Sí" : "No"
                });
            }
        }
    }
}


