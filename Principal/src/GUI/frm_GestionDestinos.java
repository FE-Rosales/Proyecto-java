    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
        // 1. Capturamos los datos de las cajas de texto (con trim() para
        //    evitar espacios accidentales que afecten futuras busquedas)
        String nombre = txtNombre.getText().trim();
        String pais = txtPais.getText().trim();
        String ciudad = txtCiudad.getText().trim();
        String desc = txtDesc.getText().trim();
        String clima = txtClima.getText().trim();
        String idioma = txtIdioma.getText().trim();
        String imagen = "ruta_default.jpg"; // Por ahora dejamos una ruta fija

        // Validación simple para no registrar campos vacíos
        if (nombre.isEmpty() || pais.isEmpty() || ciudad.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "El Nombre, País y Ciudad son obligatorios.");
            return;
        }

        // 2. Creamos el objeto DestinoTuristico con los datos capturados
        modelo.DestinoTuristico nuevoDestino = new modelo.DestinoTuristico(
            nombre, pais, ciudad, desc, clima, idioma, imagen
        );
        
        // 3. Registramos en el gestor
        if (gestorDestinos.registrar(nuevoDestino)) {
            // 4. Actualizamos la tabla visual
            javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) tblDestinos.getModel();
            modeloTabla.addRow(new Object[]{nombre, pais, ciudad, clima});
            
            // Limpiamos los campos después de registrar
            txtNombre.setText("");
            txtPais.setText("");
            txtCiudad.setText("");
            txtDesc.setText("");
            txtClima.setText("");
            txtIdioma.setText("");
            
            javax.swing.JOptionPane.showMessageDialog(this, "Destino registrado correctamente.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Capacidad máxima alcanzada.");
        }
        
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        
        
        int filaSeleccionada = tblDestinos.getSelectedRow();
    
    if (filaSeleccionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione una fila de la tabla primero.");
        return;
    }

    // Obtenemos los datos originales de la tabla para buscar en el gestor
    javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) tblDestinos.getModel();
    String nombreOriginal = (String) modeloTabla.getValueAt(filaSeleccionada, 0);
    String ciudadOriginal = (String) modeloTabla.getValueAt(filaSeleccionada, 1);

    // Creamos el nuevo objeto con los datos modificados de las cajas
    modelo.DestinoTuristico destinoModificado = new modelo.DestinoTuristico(
        txtNombre.getText(), txtPais.getText(), txtCiudad.getText(), 
        txtDesc.getText(), txtClima.getText(), txtIdioma.getText(), "ruta.jpg"
    );

    // Llamamos al método modificar que creamos en tu GestorDestinos
    if (gestorDestinos.modificar(nombreOriginal, ciudadOriginal, destinoModificado)) {
        // Actualizamos la tabla visual
        modeloTabla.setValueAt(txtNombre.getText(), filaSeleccionada, 0);
        modeloTabla.setValueAt(txtPais.getText(), filaSeleccionada, 1);
        modeloTabla.setValueAt(txtCiudad.getText(), filaSeleccionada, 2);
        modeloTabla.setValueAt(txtClima.getText(), filaSeleccionada, 3);
        
        javax.swing.JOptionPane.showMessageDialog(this, "Destino modificado correctamente.");
    }
        
        
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
         
        int filaSeleccionada = tblDestinos.getSelectedRow();
        
        if (filaSeleccionada == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Seleccione la fila que desea eliminar.");
        return;
    }
        javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) tblDestinos.getModel();
        String nombre = (String) modeloTabla.getValueAt(filaSeleccionada, 0);
        String ciudad = (String) modeloTabla.getValueAt(filaSeleccionada, 1);
        
        if (gestorDestinos.eliminar(nombre, ciudad)) {
        modeloTabla.removeRow(filaSeleccionada);
        javax.swing.JOptionPane.showMessageDialog(this, "Destino eliminado.");
        
        } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Error al eliminar.");
        
        }    
    }//GEN-LAST:event_btnEliminarActionPerformed


