    /** Carga en la tabla los destinos que ya estaban registrados, para que
     *  no se vean "perdidos" al cerrar y volver a abrir esta ventana. */
    private void cargarDestinosExistentes() {
        javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) tblDestinos.getModel();
        modeloTabla.setRowCount(0);
        for (modelo.DestinoTuristico d : gestorDestinos.listarTodos()) {
            modeloTabla.addRow(new Object[]{d.getNombre(), d.getPais(), d.getCiudad(), d.getClima()});
        }
    }

