    /** Carga en la tabla los clientes que ya estaban registrados, para que
     *  no se vean "perdidos" al cerrar y volver a abrir esta ventana. */
    private void cargarClientesExistentes() {
        javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) tblClientes.getModel();
        modeloTabla.setRowCount(0);
        java.time.format.DateTimeFormatter formato = java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy");
        for (Cliente c : gestorLocal.listarTodos()) {
            modeloTabla.addRow(new Object[]{
                c.getNombres(), c.getApellidos(), c.getDocumento(), c.getNacionalidad(),
                c.getFechaNacimiento().format(formato), c.getTelefono(), c.getCorreo()
            });
        }
    }
    
    

