    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        try {
            // 1. Capturar los textos ingresados en las cajas (con trim() para
            //    evitar espacios accidentales que impidan encontrar al cliente despues)
            String nombres = txtNombres.getText().trim();
            String apellidos = txtApellidos.getText().trim();
            String dni = txtDNI.getText().trim();
            String nacionalidad = txtNacion.getText().trim();
            String fechaNacString = txtFechaN.getText().trim(); // Lo capturamos como String temporalmente
            String telefono = txtTelefono.getText().trim();
            String correo = txtCorreo.getText().trim();

            // Validación rápida: Que no intenten registrar un cliente vacío
            if (dni.isEmpty() || nombres.isEmpty() || apellidos.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Por favor, complete al menos DNI, Nombres y Apellidos.");
                return; 
            }

            // --- LA SOLUCIÓN ESTÁ AQUÍ ---
            // Le decimos a Java qué formato vamos a usar (Día/Mes/Año)
            java.time.format.DateTimeFormatter formato = java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy");
            
            // Traducimos el texto a un objeto LocalDate real
            java.time.LocalDate fechaNac = java.time.LocalDate.parse(fechaNacString, formato);

            // 2. Crear el objeto Cliente (ahora sí los tipos coinciden)
            Cliente nuevoCliente = new Cliente(dni, nombres, apellidos, fechaNac, nacionalidad, telefono, correo);

            // 3. ¡LA CONEXIÓN! Lo enviamos al gestor para que lo guarde en el arreglo
            boolean registroExitoso = gestorLocal.registrar(nuevoCliente);

            if (registroExitoso) {
                // 4. Mostrar el nuevo cliente en la JTable de la pantalla (usamos el string original para que se lea bien en la tabla)
                javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblClientes.getModel();
                modelo.addRow(new Object[]{nombres, apellidos, dni, nacionalidad, fechaNacString, telefono, correo});

                // 5. Limpiar las cajas de texto
                txtNombres.setText("");
                txtApellidos.setText("");
                txtDNI.setText("");
                txtNacion.setText("");
                txtFechaN.setText("");
                txtTelefono.setText("");
                txtCorreo.setText("");

                javax.swing.JOptionPane.showMessageDialog(this, "Cliente registrado con éxito en el sistema.");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: Memoria llena. Capacidad máxima de clientes alcanzada.", "Error", javax.swing.JOptionPane.ERROR_MESSAGE);
            }

        } catch (java.time.format.DateTimeParseException ex) {
            // Si el usuario escribe letras o una fecha al revés, atrapamos el error para que el programa no explote
            javax.swing.JOptionPane.showMessageDialog(this, "Error: La fecha debe tener el formato exacto DD/MM/YYYY (ejemplo: 25/10/1995).", "Error de Formato", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error al registrar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        try {
            // 1. Obtenemos el modelo de la tabla
            javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblClientes.getModel();
            
            // 2. Averiguamos qué fila seleccionó el usuario con el mouse
            int filaSeleccionada = tblClientes.getSelectedRow();
            
            // 3. Validamos que realmente haya seleccionado algo (-1 significa que no hay selección)
            if (filaSeleccionada >= 0) {
                // Confirmación de seguridad antes de borrar
                int respuesta = javax.swing.JOptionPane.showConfirmDialog(this, 
                    "¿Está seguro de eliminar este cliente?", 
                    "Confirmar Eliminación", 
                    javax.swing.JOptionPane.YES_NO_OPTION);
                    
                if (respuesta == javax.swing.JOptionPane.YES_OPTION) {
                    
                    // Capturamos el DNI de la fila
                    String dniSeleccionado = tblClientes.getValueAt(filaSeleccionada, 2).toString();
                    
                    // Borramos el cliente del arreglo interno
                    boolean eliminado = gestorLocal.eliminar(dniSeleccionado);
                    
                    if (eliminado) {
                        // Borramos la fila visualmente
                        modelo.removeRow(filaSeleccionada);
                        javax.swing.JOptionPane.showMessageDialog(this, "Cliente eliminado correctamente.");
                    } else {
                        javax.swing.JOptionPane.showMessageDialog(this, "No se pudo eliminar de la memoria.");
                    }
                }
            } else {
                // Si presionó el botón sin seleccionar a nadie en la tabla
                javax.swing.JOptionPane.showMessageDialog(this, "Por favor, seleccione un cliente de la tabla primero.");
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error al eliminar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed

        try {
            int fila = tblClientes.getSelectedRow();
            
            if (fila >= 0) {
                // Validación para que no dejen campos en blanco por error
                if (txtDNI.getText().trim().isEmpty() || txtNombres.getText().trim().isEmpty()) {
                    javax.swing.JOptionPane.showMessageDialog(this, "No puede dejar el DNI o los Nombres vacíos.");
                    return;
                }

                // Traducimos el texto a un objeto LocalDate real
                java.time.format.DateTimeFormatter formato = java.time.format.DateTimeFormatter.ofPattern("dd/MM/yyyy");
                java.time.LocalDate fechaNac = java.time.LocalDate.parse(txtFechaN.getText().trim(), formato);

                // Creamos el objeto cliente con la nueva información (con trim() en cada campo)
                Cliente clienteModificado = new Cliente(
                    txtDNI.getText().trim(), txtNombres.getText().trim(), txtApellidos.getText().trim(),
                    fechaNac, txtNacion.getText().trim(), txtTelefono.getText().trim(), txtCorreo.getText().trim()
                );

                // ¡LA CONEXIÓN! Reemplazamos en la memoria a través de tu Gestor
                boolean modificado = gestorLocal.modificar(clienteModificado);

                if (modificado) {
                    // Reemplazamos los datos de esa fila con lo nuevo que se haya escrito visualmente
                    tblClientes.setValueAt(txtNombres.getText().trim(), fila, 0);
                    tblClientes.setValueAt(txtApellidos.getText().trim(), fila, 1);
                    tblClientes.setValueAt(txtDNI.getText().trim(), fila, 2);
                    tblClientes.setValueAt(txtNacion.getText().trim(), fila, 3);
                    tblClientes.setValueAt(txtFechaN.getText().trim(), fila, 4);
                    tblClientes.setValueAt(txtTelefono.getText().trim(), fila, 5);
                    tblClientes.setValueAt(txtCorreo.getText().trim(), fila, 6);
                    
                    // Limpiamos las cajas
                    txtNombres.setText("");
                    txtApellidos.setText("");
                    txtDNI.setText("");
                    txtNacion.setText("");
                    txtFechaN.setText("");
                    txtTelefono.setText("");
                    txtCorreo.setText("");
                    
                    // Deseleccionamos la fila de la tabla para que no quede marcada
                    tblClientes.clearSelection();

                    javax.swing.JOptionPane.showMessageDialog(this, "Datos del cliente actualizados con éxito.");
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Error: No se encontró el DNI en el sistema original.");
                }
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Por favor, haga clic en un cliente de la tabla primero para modificarlo.");
            }
        } catch (java.time.format.DateTimeParseException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: La fecha debe tener el formato exacto DD/MM/YYYY.", "Error de Formato", javax.swing.JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error al modificar: " + e.getMessage());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

