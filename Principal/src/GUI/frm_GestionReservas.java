    private void txtAdultosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtAdultosKeyReleased
        // TODO add your handling code here:   
    try {
        // 1. Obtenemos el paquete seleccionado
        modelo.PaqueteTuristico p = (modelo.PaqueteTuristico) cmbPaquetes.getSelectedItem();
        
        // 2. Obtenemos los números de pasajeros (si están vacíos, usamos 0)
        int adultos = txtAdultos.getText().isEmpty() ? 0 : Integer.parseInt(txtAdultos.getText());
        int menores = txtMenores.getText().isEmpty() ? 0 : Integer.parseInt(txtMenores.getText());
        
        // 3. Calculamos usando la lógica que ya tiene la clase Reserva 
        double precioBase = p.getPrecioPorPersona();
        double total = (adultos * precioBase) + (menores * precioBase * 0.5);
        
        // 4. Se muestrra en el campo de texto
        txtTotal.setText(String.format("%.2f", total));
        
    } catch (Exception e) {
        // Si el usuario escribe letras en vez de números, no hacemos nada o limpiamos
        txtTotal.setText("0.00");
    }    
    }//GEN-LAST:event_txtAdultosKeyReleased

