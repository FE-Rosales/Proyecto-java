    private void btnAgregarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarClienteActionPerformed
        // TODO add your handling code here:
        
        String dniBuscado = txtDniCliente.getText().trim();
    
    if (dniBuscado.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingrese un DNI.");
        return;
    }

    // Buscamos al cliente usando el gestor que pasamos por el constructor
    // NOTA: Asegúrate de que el método se llame 'buscarCliente' o similar en tu GestorClientes
    modelo.Cliente clienteEncontrado = gestorC.buscarPorDocumento(dniBuscado);

    if (clienteEncontrado != null) {
        // Agregamos el cliente a la tabla visual
        javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) tblClientesReserva.getModel();
        
        // Asumiendo que tu tabla tiene columnas: DNI, Nombre, Apellido
        modeloTabla.addRow(new Object[]{
            clienteEncontrado.getDocumento(), 
            clienteEncontrado.getNombres(), 
            clienteEncontrado.getApellidos()
        });
        
        // Limpiamos la caja de texto para buscar otro
        txtDniCliente.setText("");
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Cliente no encontrado. Regístrelo en el módulo de Clientes primero.");
    }
        
    }//GEN-LAST:event_btnAgregarClienteActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        
        try {
        // 1. Validamos que haya un paquete seleccionado
        if (cmbPaquetes.getSelectedItem() == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "Seleccione un paquete turístico.");
            return;
        }
        modelo.PaqueteTuristico paqueteSel = (modelo.PaqueteTuristico) cmbPaquetes.getSelectedItem();

        // 2. Obtenemos la cantidad de pasajeros
        int adultos = txtAdultos.getText().isEmpty() ? 0 : Integer.parseInt(txtAdultos.getText());
        int menores = txtMenores.getText().isEmpty() ? 0 : Integer.parseInt(txtMenores.getText());
        
        if (adultos == 0 && menores == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar al menos un pasajero.");
            return;
        }

        // 3. Obtenemos los clientes de la tabla y armamos el arreglo que pide el gestor
        javax.swing.table.DefaultTableModel modeloTabla = (javax.swing.table.DefaultTableModel) tblClientesReserva.getModel();
        int cantidadClientesTabla = modeloTabla.getRowCount();
        
        if (cantidadClientesTabla == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe agregar al menos un cliente a la tabla.");
            return;
        }

        modelo.Cliente[] grupoClientes = new modelo.Cliente[cantidadClientesTabla];
        for (int i = 0; i < cantidadClientesTabla; i++) {
            // Asumiendo que el DNI está en la columna 0 de tu tabla
            String dniFila = modeloTabla.getValueAt(i, 0).toString(); 
            // Buscamos el objeto Cliente completo usando tu GestorClientes
            grupoClientes[i] = gestorC.buscarPorDocumento(dniFila);
        }

        // 4. Asesor que gestiona la reserva (el que inicio sesion, recibido por constructor)
        // 5. LLAMAMOS AL GESTOR QUE TE PASARON (Aquí ocurre la magia)
        // Ojo: Asegúrate de que tu Jframe ya tenga la variable 'gestorR' (tu GestorReservas) instanciada.
        modelo.Reserva nuevaReserva = gestorR.crearReserva(
            paqueteSel, 
            asesorActual, 
            grupoClientes, 
            adultos, 
            menores, 
            java.time.LocalDate.now()
        );

        // 6. Verificamos si la reserva se creó con éxito o si rebotó por falta de cupo
        if (nuevaReserva != null) {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "¡Reserva confirmada exitosamente!\nTotal a pagar: S/ " + nuevaReserva.getPrecioTotal());
                
            // Limpiamos la pantalla para una nueva venta
            modeloTabla.setRowCount(0);
            txtAdultos.setText("");
            txtMenores.setText("");
            txtTotal.setText("");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, 
                "Error: No hay cupos suficientes en el paquete o se alcanzó el límite de reservas.");
        }

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingrese números válidos en los campos de pasajeros.");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Ocurrió un error inesperado: " + e.getMessage());
    }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed


