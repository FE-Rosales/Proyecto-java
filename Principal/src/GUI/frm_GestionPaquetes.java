    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here: 
        try {
        // 1. Textos básicos
        String codigo = txtCodigo.getText().trim();
        String nombre = txtNombre.getText().trim();

        // 2. Números (Días, noches, precio y cupos)
        int dias = Integer.parseInt(txtDias.getText().trim());
        int noches = Integer.parseInt(txtNoches.getText().trim());
        double precio = Double.parseDouble(txtPrecio.getText().trim());
        int cuposMax = Integer.parseInt(txtCupos.getText().trim());

        // 3. Fechas
        java.time.LocalDate fechaSalida = java.time.LocalDate.parse(txtFechaSalida.getText().trim());
        java.time.LocalDate fechaRetorno = java.time.LocalDate.parse(txtFechaRetorno.getText().trim());

        // 4. Tipo de Paquete 
        modelo.TipoPaquete tipoSel = (modelo.TipoPaquete) cmbTipoPaquete.getSelectedItem();

        // 5. 
        modelo.PaqueteTuristico nuevoPaquete = new modelo.PaqueteTuristico(
            codigo, 
            nombre, 
            tipoSel, 
            dias, 
            noches, 
            precio, 
            fechaSalida, 
            fechaRetorno, 
            cuposMax
        );

        // 6. Guarda en el Gestor
        boolean exito = gestorP.registrar(nuevoPaquete);

        if (exito) {
            javax.swing.JOptionPane.showMessageDialog(this, "¡Paquete registrado exitosamente!");
            actualizarTabla();
            // Limpiamos las cajas
            txtCodigo.setText("");
            txtNombre.setText("");
            txtDias.setText("");
            txtNoches.setText("");
            txtPrecio.setText("");
            txtCupos.setText("");
            txtFechaSalida.setText("");
            txtFechaRetorno.setText("");
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: Se alcanzó el límite máximo de paquetes.");
        }

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "ERROR: Verifique que Días, Noches, Precio y Cupos sean números válidos.");
    } catch (java.time.format.DateTimeParseException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "Error: La fecha debe tener el siguiente formato (ejemplo: 2026-10-15).");
    } catch (Exception e) {
        javax.swing.JOptionPane.showMessageDialog(this, "ERROR: " + e.getMessage());   
    }//GEN-LAST:event_btnRegistrarActionPerformed
    }
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        
        // 1. Obtener el código de la caja de texto
    String codigo = txtCodigo.getText().trim();

    if (codigo.isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Por favor, ingrese el Código del paquete que desea eliminar.");
        return;
    }

    // 2. Pedir confirmación de seguridad (¡Muy importante en Ingeniería de Software!)
    int confirmacion = javax.swing.JOptionPane.showConfirmDialog(this, 
        "¿Está seguro que desea eliminar el paquete con código: " + codigo + "?", 
        "Confirmar Eliminación", 
        javax.swing.JOptionPane.YES_NO_OPTION,
        javax.swing.JOptionPane.WARNING_MESSAGE);

    // 3. Si el usuario hace clic en "Sí" (YES_OPTION)
    if (confirmacion == javax.swing.JOptionPane.YES_OPTION) {
        
        // Llamamos a tu GestorPaquetes
        boolean exito = gestorP.eliminar(codigo);

        if (exito) {
            javax.swing.JOptionPane.showMessageDialog(this, "Paquete eliminado correctamente.");
            actualizarTabla();
            // Limpiamos la caja de texto
            txtCodigo.setText("");
            txtCodigo.requestFocus();
            
            // Aquí luego llamaremos a actualizarTabla() para que desaparezca visualmente
            
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Error: No se encontró ningún paquete con ese código.");
        }
    }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

