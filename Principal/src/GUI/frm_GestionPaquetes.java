    private void llenarCombos() {
        cmbTipoPaquete.removeAllItems();
        for (modelo.TipoPaquete tipo : modelo.TipoPaquete.values()) {
            cmbTipoPaquete.addItem(tipo);
        }
        
        cmbEstado.removeAllItems();
        for (modelo.EstadoPaquete estado : modelo.EstadoPaquete.values()) {
            cmbEstado.addItem(estado);
        }
    }
    
    private void actualizarTabla() {
    // 1. Obtenemos el modelo de tu tabla
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblPaquetes.getModel();
    
    // 2. Limpiamos la tabla para no duplicar datos visualmente
    modelo.setRowCount(0);
    
    // 3. Le pedimos a tu gestor TODOS los paquetes registrados
    modelo.PaqueteTuristico[] lista = gestorP.listarTodos();
    
    // 4. Recorremos el arreglo y agregamos fila por fila
    if (lista != null) {
        for (modelo.PaqueteTuristico p : lista) {
            if (p != null) { // Verificación de seguridad
                modelo.addRow(new Object[]{
                    p.getCodigo(),
                    p.getNombre(),
                    p.getTipo(),
                    p.getDuracionDias(),
                    p.getDuracionNoches(),
                    p.getPrecioPorPersona(),
                    p.getCupoDisponible() + "/" + p.getCupoMaximo(),
                    p.getEstado()
                });
            }
        }
    }
}

